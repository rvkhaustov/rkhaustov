public class Block {
    /**
     * x.
     */
    private int x;
    /**
     * y.
     */
    private int y;

    /**
     * @param x x
     * @param y y
     */
    public Block(int x, int y) {
        this.x = x;
        this.y = y;
    }

    /**
     * @return x
     */
    public int getX() {
        return x;
    }

    /**
     * @return y
     */
    public int getY() {
        return y;
    }

}
